def add_numbers(x, y):
    """
    Add two numbers.
    
    Parameters:
    x (int): First number to be added.
    y (int): Second number to be added.
    
    Returns:
    int: The sum of the two numbers.
    """
    return x + y

def multiply_numbers(x, y):
    """
    Multiply two numbers.
    
    Parameters:
    x (int): First number to be multiplied.
    y (int): Second number to be multiplied.
    
    Returns:
    int: The product of the two numbers.
    """
    return x * y

# Example usage
result_addition = add_numbers(3, 4)
result_multiplication = multiply_numbers(5, 6)

print("Addition:", result_addition)
print("Multiplication:", result_multiplication)
